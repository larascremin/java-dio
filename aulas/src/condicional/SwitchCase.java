package condicional;

public class SwitchCase {

    public static void main(String[] args) {

        int numero = 10;

        switch (numero) {
            case 2: {
                System.out.println(numero);
                break;
            }
            case 6: {
                System.out.println(numero);
                break;
            }
            default: {
                System.out.println("indefinido");
            }
        }

    }

}
